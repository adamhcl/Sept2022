package com.saran.app;
//Number 5
public class Number5 {
	public static void main(String[] args) {
		//using for loop to print even numbers between 1 and 100.
		for(int i=1;i<=100;i++) {
			//checking if number is even and if so, prints that value.
			if (i % 2 == 0) {
				System.out.println(i);
			}
			
		}
			
	}
}